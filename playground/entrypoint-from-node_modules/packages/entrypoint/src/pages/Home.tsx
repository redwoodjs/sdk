"use client";

import { RequestInfo } from "rwsdk/worker";
import { useState } from "react";

export function Home({ ctx }: RequestInfo) {
  const [state, setState] = useState(0);

  return (
    <>
      <div>Hello World</div>

      <button
        onClick={() => setState((prevState) => prevState + 1)}
      >
        Clicks: {state}
      </button>
    </>
  );
}
